    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $produk = Produk::where('id_produk', $request->id_produk)->first();
        if (! $produk) {
            return response()->json('Data gagal disimpan', 400);
        }
        $transaksi = Transaksi::where('id_transaksi', $request->id_transaksi)->first();
        if (! $transaksi) {
            return response()->json('Data gagal disimpan', 400);
        }
        $transaksidetail = new TransaksiDetail();
        $transaksidetail-> id_transaksi = $transaksi->id_transaksi;
        $transaksidetail-> id_produk = $produk->id_produk;
        $transaksidetail-> harga = $produk->harga;
        $transaksidetail-> jumlah = 1;
        $transaksidetail-> subtotal= $produk->harga;
        $transaksidetail->save();
        
        return response()->json('Data berhasil disimpan', 200);
    }

